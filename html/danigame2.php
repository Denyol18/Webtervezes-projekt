            <p>
                A játékmenet és a történetszál szorosan összefonódik, meggyőző módon, összhangban a játék menetével.
                A játék lehetőséget nyújt a játékosoknak, hogy különböző szárazföldi és légi járműveket irányíthassanak third person nézetben,
                Ez a nézet az elmélyülés örömteli érzését kelti,
                és kiemeli a játékmenet bizonyos pontjait, ismét megkülönböztetve a Halo-t a kortárs lövöldözős játékoktól.
                A Halo új fegyverrendszere két fő szempontból is egyedülálló. Az első az, hogy a játékosok egyszerre csak két fegyvert hordhatnak maguknál, így kompromisszumokra kényszeríti őket a játék.
                A második változás egy külön gomb a gránátok dobására.
                Az AI meglehetősen kifinomult a korszakához képest. A vadonatúj, fejlett mesterségesintelligencia-rendszerrel az AI által végrehajtott műveletek, mint például a pánik egy bajtársa halála után, az elugrás a szembejövő jármű elől,
                vagy a robbanóanyagok és a lövedékek elől való fedezék keresés, segített a Halonak kitűnni a többi játék közül.

            </p>
            <p>
                A játékban való mozgás hasonló a többi fps játékhoz, lehetővé téve a játékos számára, hogy leguggoljon és
                ugráljon, bár a magas helyekről való ugrás gyakran halált, vagy legalábbis súlyos fall damaget okoz. Az esés okozta sebzés
                csökkenthető vagy teljesen kiküszöbölhető egy jól időzített leguggolással, amikor az ember földet ér, így kijátszva a rendszert.
                Továbbá, ha a játékos egy szikláról ugrik le és zuhanás közben rendszeresen megérinti a falat. az annak számít, mintha földet érintene,
