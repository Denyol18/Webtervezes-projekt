            <h2>Bevezető</h2>
            <p>
                A Halo franchise egy maghátorozó része az fps genre-nak és ezt a legelső játék alapozta meg, ami nagyon sok új
                dolgot mutatott akkoriban amiért találó a Combat Evolved alcím. A Halo: Combat Evolved, más néven Halo 1 vagy Halo: CE egy First Person Shooter sci-fi videojáték,
                amelyet a Bungie Studios hozott létre,
                amely akkoriban a Microsoft Game Studios leányvállalata volt. A játékot az eredeti Xbox játékkonzolra 2001. november 15-én adták ki.
                2003-ban Halo PC-ként is megjelent PC-re és Mac-re.
            </p>

            <p>
                A Halo minden idők egyik legnépszerűbb játéka az eredeti Xbox-on az ötmillió eladott példányával,
                amivel csak a folytatás, a Halo 2 vetekszik, amely nyolcmilliós eladást ért el.
            </p>

