            <h2>Bossok</h2>

            <img class="kepjobbrasmol broder" title="Nem mennék hozzá azért hurkáért" alt="Where Butcher" src="../images/butcher.jpg">

            <p>
                A <strong>Butcher</strong> egy nagydarab, disznófejű, humanoid főnök. Véres kötényt viselő, bőréből iszonyatos módon fa- és fémrészek állnak ki.
                A Butcher egy nagy húshoroggal van felszerelve, amelyet hatalmas sebzés érdekében lendít, és néhány ütéssel képes leütni a játékost. A Butcher távolról is tud tűzgolyókat dobni, és immunis minden fire damage-re. Figyelni kell a frenzy módra, amit akkor ha sok sebzést kapott már, amely extra sebzést és ellenállást biztosít a hentesnek.
                Ha el akarjuk távolítani ezt a túlméretezett sertéskártevőt, javasoljuk, hogy használj shotgunt és explosive-ot. Ha találsz egyet, és közelebb kerülsz, akkor a Butcher gyengepontja a nagyobb melee fegyverek, amiket lehet találni a boss termében, ami azt jelenti, hogy egy fejsze hatalmas sebzést okoz egyetlen csapással.
                Alacsony mobilitása miatt az javasolt, hogy a Butcher elől fussunk el, amikor kifogyott a fegyver és tölteni kell. Két játékos esetén ez sokkal könnyebbé válik, hagyjuk, hogy az egyik újratöltse, míg a másik elcsalogatja. Amikor a Butcher meghal a halál animációjában csap még egyet amivel ha annyira kevés életünk van, akkor meg is tud ölni azzal. Revenge mada faka.

            </p>

            <img class="kepbalrasmol broder" title="Belőle nem nyelünk le álmunkban ötöt, hamarabb fulladunk meg tőle" alt="Where Spider" src="../images/spider.jpg">

            <p>
                Az arachnofóbiában szenvedők a legjobban megteszik, ha elkerülik a Hunt: Shodown <strong>Spider</strong> boss-t, mivel pontosan azt kínálja, ami a dobozon van. Hosszú, szőrös lábak nyúlnak ki ebből az óriási, sikoltozó lényből.
                Hihetetlenül mozgékony, immunis a poison sebzésekre.
                A Spider szereti az termének falait és padlóját használni mozgásra miközben méreggolyókat köp a játékosra. Ezek méregfelhőket hagynak maguk után, amelyek sebeznek időszakosan ha benne állunk, így jobb, ha távol maradunk belőlük. 
                A Spider pounce támadást is alkalmaz, a padlót kaparja, és hangos sikoltást ad ki, mielőtt rád veti magát. Ha eleget sebezzük a Spider-t a "rohama" alatt, akkor megszakad. Végül a Spider frenzy módba lép, amikor alacsony az élete, akkor növekszik a sebessége és védelme, miközben üldözi a játékost, és karmaival szúrja őket.
                Javasolott, hogy puskákat és lámpásokat használjunk a Spider ellen, mert mind a tüzelésre, mind a tompa sebzésre gyenge, ezért keressen robbanásveszélyes csöveket az aréna körül, és robbantsa fel őket, amikor a Spider közel van. Amint megtanulod a mozgásmintázatait, a Sledgehammer erőteljes fegyverré válik ezzel a boss-szal szemben, és körülbelül hat ütéssel legyőzhető. 
                Ha észreveszed a réseket miközben a Pók rikoltoz, használjuk ezt az erős közelharci fegyvert a gyorsabb, csendesebb küzdelem érdekében.
            </p>

            <img class="kepjobbrasmol broder" title="Ő már nem fog küzdeni a templomosok ellen" alt="Where Assassin" src="../images/assassin.jpg">

            <p>
                A Hunt: Showdown bossainak arzenáljának legújabb tagja, az <strong>Assassin</strong> egy gyors, karcsú humanoid figura, amely több száz apró rovarból áll. Az Assassin az egyik legnehezebben követhető főnök, mivel cserél az emberi alak és a kavargó bogarak között. Mindkét formában képes sebzést okozni, de csökken a sebzése, miközben egy rajban van.
                Az Assassin fő támadása szúrások sorozata, amelyek gyorsan a játékos felé haladnak, és előrecsapnak, mielőtt visszavonulnának. Ezenkívül távolsági támadást is indít splash sebzéssel, amely összehangolt erőfeszítést igényel a kikerüléshez.
                Az Assassin klónokat is létrehozhat, amelyek megzavarják a játékos látását rovarokkal, bár ezeket könnyen elpusztítják távolsági vagy közelharci támadások. Amikor az Assassin frenzy módba lép, két klónt hoz létre, amelyeket a játékos meg tud semmisíteni.
                A legjobb módszer az Assassin elleni küzdelemhez, ha megvárjuk, hogy az előre támadjon egy támadásra. Ennél a pontnál töltsünk bele ólmot, hogy megszakítsd a támadását, nagy sebzést okozva. Pusztítsd el a klónjait, amikor megjelentek, majd várd meg, amíg az Assassin újra támad.
                Az Assassin gyenge a mérgezéshez, és könnyen megszakítható, feltéve, hogy eltalálod a fegyvereddel. Keressünk lámpásokat és robbanóanyagokat az aréna környékén, és ezekkel gyújtsd fel őt a további sebzésért folytatott küzdelem során. 
                Dobj méregbombákat, hogy megcélozd a gyengeségét, erre egy jó módszer, ha benyomunk magunka egy méreg elleni oltást és beleállunk a méreg felhőbe, az Assasin attól még támadni fog. Ha magabiztosnak érezzük magunkat a közelharci fegyverek használatában, a fejsze és a kalapács nagyszerű módja annak, hogy gyorsan és csendesen leszedje.
            </p>

