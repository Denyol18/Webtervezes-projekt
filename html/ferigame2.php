            <p>
                A Hunt: Showdown játékmenetét számos olyan funkció jellemezte, amelyek megkülönböztetik a korabeli fps játékoktól. 
                A játék nagyon részletes hangokkal rendelkezik, mindennek van valami hangja, amit nem csak mi hallunk hanem a külvilág is, ajánlott teljes hangerővel játszani, hogy ne maradjunk
                le a legkisebb nesszről sem.

            </p>
            <p>
                A játék folyamán, mint már említve lett Bossok-ra illetve játékosokra lehet vadászni. Ahhoz, hogy elkezdhessünk egy meccset rendelkeznünk kell egy vadásszal,
                akit meg tudunk venni és ő vele fogunk játszani addig, amíg meg nem hal egy vadászat során, vagy amíg mi nem küldjül el nyugdíjba, mind a kettő esetben elveszi 
                az összes nála lévő tárgyat, fegyvert stb. Ha elmenekülünk a vadászatból akkor kapunk az alapján, hogy mennyi játékost, játékbeli ellenfelet, boss-t öltünk meg, illetve 
                mennyi boss trófeával hagytuk el a pályát kapunk pénzt és tapasztalati pontot a vadásznak illetve a felhasználónk számára. A pénzből tárgyakat, fegyvereket illetve drágább vadászokat
                vásárolhatunk, amik alapból jobb fegyverekkel illetve perk-ekkel rendelkeznek. Ha már perk-ek, a felhasználó fejlődésével oldhatunk fel újabbakat, amiket a vadásszal minden egyes szintje lépéséért kapott
                skill pointtal tudunk felszerelni az adott vadásznak, amik segítséget adnak a játék során, pl.: gyorsabban tudunk a vízben futni vagy hangtalanul tudunk fegyvert váltani.
            </p>
            <p>
                A meccsek folyamán ha boss-okra szeretnénk vadászni, akkor követnünk kell a nyomaikat (amiért szintén pénz jár), és ez által tudunk szűkíteni a térképünkön a 
                területen, hogy hol lehet esetleg lehetnek még. Minden vadászaton minimum 1, maximum 2 boss lehet, akibe ha mázlink van bele is futhatunk az egyik névvel ellátott helyen, mert csak olyan helyeken lehet megtalálni.
