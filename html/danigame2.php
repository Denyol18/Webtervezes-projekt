            <h2>Kampány</h2>

            <img class="broder kepjobbra" title="Egy igen ritka látvány: a Master Chief fegyver nélkül." alt="Where Master Chief" src="../images/halo5.jpg">

            <p>
                A Halo: Combat Evolved 26. századi sci-fi környezetben játszódik. A fénynél gyorsabb utazás, az úgynevezett Slipspace lehetővé teszi az emberi faj számára,
                hogy a Földön kívül más bolygókat is kolonizáljon.
                A Reach bolygó a tudományos és katonai tevékenységek csillagközi központjaként szolgál. A United Nations Space Command (UNSC) titkos programot indít, amiben
                a Spartans néven ismert szuperkatonákat hozzák létre. Több mint húsz évvel a játék kezdete előtt a Covenant nevű idegen fajok technológiailag fejlett kollektívája vallási háborúba kezd
                az emberiség ellen, és isteneik megsértésének nyilvánítja az emberek puszta létezését. Az emberiség katonasága megsemmisítő vereségek sorozatát éli át; bár a szuperkatonák hatékonyak a
                szövetséggel szemben, túl kevesen vannak ahhoz, hogy megfordítsák a helyzetet. 2552-ben a Covenant erői megtámadják Reach-et és elpusztítják a kolóniát.
                A Pillar of Autumn csillaghajó megszökik a bolygóról és ugrást kezdeményez az űrbe, remélve, hogy eltereli az ellenség figyelmét.

            </p>
            <p>
                Az űrhajó kiemelkedik a Slipspace-ből, és egy hatalmas gyűrűt talál – az űrben lebegő „Halo”-t. Így kezdődik a játék.
                A főszereplő szuperkatona, John-117, másnéven a Master Chief a Pillar of Autumnön tartózkodik, amikor a hajót megtámadja a Covenant, akik követték a hajót.
                John-117 evakuálja a hajón tartózkodókat a Halo felszínére, hogy megvédje az Autumn mesterséges intelligenciáját, Cortanát,
                aki nagyon fontos katonai információkat hordoz, amelyek ha a Covenant kezébe kerülne, akkor az katasztrófához vezetne. Földet érve a UNSC tengerészgyalogságával az oldalán,
                Cortana útmutatást adva, géppuskáját készenlétben tartva és feltöltve,
                a Master Chief elindul, hogy kiderítse a Halo titkait, legyőzze a Covenant erőit és visszajusson a Földre.
            </p>

